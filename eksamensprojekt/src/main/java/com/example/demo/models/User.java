package com.example.demo.models;

//Vores inheritance klasse :))
public class User {

    private String name;
    private String mail;
    private String phonenumber;

    public User(){};

    public User(String name, String mail, String phonenumber){
        this.name = name;
        this.mail = mail;
        this.phonenumber = phonenumber;
    }
}
